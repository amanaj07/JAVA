package string;

public class indexof {
    public static void main(String[] args) {
        String s=("l a jddi wuuvlbii jsbciuasbc");
        //ye indexof charecter wala pure ek string me ek charecter ka bata hai

        System.out.println(s.indexOf("a j"));
        // or ye indexof string wala kai sari string me se ksisis ek ke starring ka index abat hai

        System.out.println(s.indexOf("wuu"));

        System.out.println(s.lastIndexOf("j"));


    }
}
