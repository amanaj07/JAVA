package conditionalandloop;

public class loops {
    public static void main(String[] args) {
        int i=0;
        int n=5;

        for (i=0;i<n;i++){
            System.out.println(i);
        }
        int j=0;
        int m=4;
        while(j<=m){
            System.out.println(j);
            j++;
        }
    }
}
